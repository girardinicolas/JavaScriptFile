const str = "Ciao mondo";        // Stringa
const num = 42;                  // Numero
const bool = true;               // Booleano
const nulla = null;              // Null
let indefinito;                  // Undefined (non inizializzata)

console.log("str è di tipo:", typeof str);
console.log("num è di tipo:", typeof num);
console.log("bool è di tipo:", typeof bool);
console.log("nulla è di tipo:", typeof nulla);
console.log("indefinito è di tipo:", typeof indefinito);
